void PrintImmage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) //выводим массив на печать
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            // Console.Write($"{matr[i, j]} ");
            if (image[i, j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}
void FillImmage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImmage(row - 1, col);
        FillImmage(row, col - 1);
        FillImmage(row + 1, col);
        FillImmage(row, col + 1);
    }
}

